						<small>Posted on 26<sup>th</sup> November 2017</small>
							<h2>20 Amazing Photography Tips and Techniques</h2>
						</div>
						<p><big>As a photographer, there are few things more satisfying than mastering a new photography technique. Learning new photography techniques can open up new possibilities, provide endless potential for inspiration, and can even help to get you out of a creative rut.</big></p>
						<p>Ready to discover some new techniques that you may not have known your camera was capable of? We have compiled a list of some nifty new photography tricks, as well as some tried and true favorites that you may – or may not – have mastered yet. Your camera has more to it than first meets the eye; get ready to impress yourself with some of these tips and techniques!</p><br>
						
						<h3>1. High Speed Photography</h3>
						<img class="featureImg img-responsive img-center" src="img/article/article4.jpg">
						<p>One of the things that makes high speed photography most intriguing is that it freezes moments in time that are too fast for the naked eye to see. The trick to high speed photography is a tripod, a narrow aperture, a flash, and lots of patience.</p>
						
						<h3>2. Night Photography</h3>
						<img class="featureImg img-responsive img-center" src="img/article/article4.1.jpg">
						<p>The fun starts when the sun goes down! There is plenty of life to capture after dark. For high-quality night photography, you will need a super-slow shutter speed, and a tripod or solid surface to steady your camera for long exposures. Don’t forget to experiment with the rule of thirds as well, good old composition techniques are still valid here.</p>
						
						<h3>3. Motion Blur</h3>
						<img class="featureImg img-responsive img-center" src="img/article/article4.2.jpg">
						<p>Motion blur is the art of capturing a moving subject. This technique is often used in sports photography, but can also be used to create interesting images with light or fast moving objects. To effectively capture motion blur, slow down your shutter speed, and hold your camera still as your subject moves. You can also “pan” with the camera, moving it along to follow the subject before releasing the shutter. This will keep your subject in focus while blurring out the background.</p>
						
						<h3>4. Black and White Photography</h3>
						<img class="featureImg img-responsive img-center" src="img/article/article4.3.jpg">
						<p>Black and white photography, when done effectively – conveys deep emotion or drama. But there’s more to black and white photography than just hitting the black and white filter in Photoshop. Effective B&W photography starts with the composition, making use of shadows, lighting, and strong subjects to create powerful imagery.</p>
						
						<h3>5. Monochromatic Color Photography</h3>
						<img class="featureImg img-responsive img-center" src="img/article/article4.5.jpg">
						<p>Monochromatic color photography captures images in a single hue or color. Much like black and white photography, monochromatic photography has a powerful way of conveying a message without much detail.</p>
						
						<h3>6. Smoke Art Photography</h3>
						<img class="featureImg img-responsive img-center" src="img/article/article4.6.jpg">
						<p>Smoke art photography can be a challenging, yet extremely rewarding technique. Smoke trails create mysterious and captivating images that are intriguing, fascinating, and fun to capture.</p>
						
						<h3>7. Macro Photography</h3>
						<img class="featureImg img-responsive img-center" src="img/article/article4.7.jpg">
						<p>Macro photography is rewarding and unique. It’s exciting to make huge images of subjects that are tiny, capturing minute details that are invisible to the human eye. The best way to make your macro photography stand out is to find subjects that are interesting and detailed. You can accomplish macro photography with just about any camera any lens, though the quality of the resulting images will vary depending on the equipment you use.</p>
						
						<h3>8. Long Exposure Photography</h3>
						<img class="featureImg img-responsive img-center" src="img/article/article4.8.jpg">
						<p>Long exposure photography can be used to achieve all kinds of interesting results, but it’s particularly popular when it comes to landscapes. A longer exposure effectively blurs movement, resulting in silky smooth water or soft, streaking clouds.</p>
						
						<h3>Forced Perspective Photography</h3>
						<img class="featureImg img-responsive img-center" src="img/article/article4.9.jpg">
						<p>Forced perspective is a fun, visual play on depth perception – with sometimes hilarious results! When composing a forced perspective image, make sure your main subject is either larger or smaller than everything else. This involves experimenting with interesting angles, and positions for your subjects. Have fun!</p>
						
						<h3>Panoramic Photography</h3>
						<img class="featureImg img-responsive img-center" src="img/article/article4.10.jpg">
						<p>Even if you don’t have an expensive camera, you can capture panoramic photographs through a technique known as “panorama stitching”. This technique involves using a tripod, and taking a series of side-by-side photographs, and merging them together later on.</p>
						
						<h3>11. Traffic Light Trails</h3>
						<img class="featureImg img-responsive img-center" src="img/article/article4.11.jpg">
						<p>Traffic light trails are a popular subject, and a great way to gets started with long exposure photography. To capture light trails, find a high traffic area in an otherwise dark location, set your tripod up, and use a slow shutter speed to blur the motion of the lights.</p>
						
						<h3>12. Painting with Light</h3>
						<img class="featureImg img-responsive img-center" src="img/article/article4.12.jpg">
						<p>Similar to traffic light trails, painting with light uses a long exposure to create illuminated “trails of light”. This fun technique can be used to create artistic images, or to add a creative spin to a photoshoot. All you need is a dark location, a slow shutter speed, and a source of moving light such as a flashlight or sparkler.</p>
						
						<h3>13. Silhouettes</h3>
						<img class="featureImg img-responsive img-center" src="img/article/article4.13.jpg">
						<p>Silhouette photography is a fun and unique way to create dramatic photos. The best time for silhouettes is sunset or sunrise. To create the silhouette effect, position your subject in front of the sun, and shoot into the sun. A faster shutter speed will result in darker exposure, and a slower shutter speed will create a brighter image.</p>
						
						<h3>14. Vintage Photography</h3>
						<img class="featureImg img-responsive img-center" src="img/article/article4.14.jpg">
						<p>A cheap UV filter, and a dab of Vaseline is all you need to create slightly distorted, vintage-style photographs.</p>
						
						<h3>15. Pinhole Photography</h3>
						<img class="featureImg img-responsive img-center" src="img/article/article4.15.jpg">
						<p>Pinhole photography is actually the foundation of modern photography. A pinhole camera is a simple camera with no lens, and a single small aperture. Light passes in through this single point, and produces images. You can make a pinhole lens for your camera by drilling a small hole in a body cap. Have fun taking beautiful, motion blurred photos.</p>
						
						<h3>16. Reflection and Mirror Photography</h3>
						<img class="featureImg img-responsive img-center" src="img/article/article4.16.jpg">
						<p>Reflections can add a powerful sense of depth to a composition. While the most common reflective photographs take place over a body of water, reflections can be effective for many different settings. Try to use a small aperture such as f/11 or higher to help even out the subjects in the shot, enhancing the reflective effect.</p>
						
						<h3>17. Water Drop Photography</h3>
						<img class="featureImg img-responsive img-center" src="img/article/article4.17.jpg">
						<p>Water drop photography freezes the action of a drop of water, creating interesting and artistic images. With water drop photography, you will want a small aperture, something like f/11 or f/14, to keep the surface of the water in focus. Use a flash, or a fast shutter speed such as 1/160 or faster to freeze the action.</p>
						
						<h3>18. Zoom Blur Effect</h3>
						<img class="featureImg img-responsive img-center" src="img/article/article4.18.jpg">
						<p>The zoom blur effect is a simple way to add some creative looking “fractured-blur” effects to your photographs. To achieve the zoom blur effect, set a slow shutter speed and zoom in or out while releasing the shutter.</p>
						
						<h3>19. Lens Flare Effects</h3>
						<img class="featureImg img-responsive img-center" src="img/article/article4.19.jpg">
						<p>Getting lens flare into your photographs can be an unhappy accident if it’s unplanned. But learning to manipulate light bursts is a great way to add an interesting bit of flair to your photographs. You can achieve this effect by shooting towards the sun, and using different angles to manipulate the light and create a burst of light around your subjects.</p>
						
						<h3>20. Kinetic Photography</h3>
						<img class="featureImg img-responsive img-center" src="img/article/article4.20.jpg">
						<p>Kinetic photography, also known as “camera toss photography”, is the art of capturing a photograph – while your camera is airborne! While this technique is a bit risky, it can result in some amazing photographs. Ideally, you only want to toss your camera a few inches in the air, and of course, ideally you will want to catch your camera on the way back down!</p><br>
						
						<p><big>One of the great things about mastering different photography tips and techniques is finding new ways to incorporate them into everyday compositions. Using the techniques that we mentioned above, you can enhance everyday subjects, and turn ordinary opportunities into creative and artistic images that really stand out.</big></p>
						

						
